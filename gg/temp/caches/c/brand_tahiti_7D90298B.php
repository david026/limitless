		<h3>Qualität der Tahitiperlen</h3>
		<p>
Die beiden wichtigesten Klassifikationssystemen, das AAA-A System und das A-D System, das auch "Tahiti System" genannt wird, werden gemeinhin verwendet. Beide sind die beliebstesten Systeme und werden von fast allen Perlenhändler akzeptiert, sowohl Groß- als auch Einzelhandel.
<BR /><BR />
Die in französisch Polynesien angewendete A-D Skala (auch Tahiti System genannt), hilft Schmuckwarenhändlern beim Einkauf von derartigen Zuchtperlen. A ist in dieser Skala die höchste Qualitätsstufe und bewertet Südsee-oder Tahitiperlen der feinsten Qualität. Anderen Marktregionen benutzen das AAA-A System weiterhin, während das A-D System in Erzeugerländern standardmäßig eingesetzt wird.
<BR /><BR />
Weitere Informationen über das System erhalten Sie unter dem Link zu <a href="http://www.perle-schmuck.com/artikel-82-Tahiti-Perlen.html" title="Tahitiperlen Bewertungssystem">Tahitiperlen Bewertungssystem</a>, der Ihnen einen Einblick in Hintergrungwissen der Tahitiperlen verschafft.</p>
