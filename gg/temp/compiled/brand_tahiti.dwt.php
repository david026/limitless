  	<h3>Über Tahiti Perlen</h3>
	<p>
	Wo werden die Tahitiperlen gewonnen?
<BR />
Die <a href="http://www.perle-schmuck.com/perlenarten-16-c0-tahitiperlen.html" title="Tahitiperle">Tahitiperle</a> ist eine Perle, die aus Beständen der Schwarzlippigen Perlenauster (Pinctada margaritifera cumingii) in Französisch-Polynesien gewonnen wird. Die Auster kann sehr groß(bis über 12 Zoll) und auch mit oft 10 Pfunde unheimlich schwer erwartet werden, wodurch häufig die weit größer als der Durchschnitt Perlen gewonnen werden. Die Tahitiperlen sind einzigartig wegen ihrer natürlichen dunklen Farbe. Die meisten "schwarze" Tahitiperlen sind nicht wirklich schwarz, sondern silber, Anthrazit oder eine Vielzahl von Farben in welcher Grün die dominierende Farbe ist. Wirklich schwarze Perlen gehören zu den schönsten Perlen der Welt und sind sehr selten.
<BR />
<BR />
Merkmal Tahitiperlen:<BR />
Kern: kernhaltig<BR />
Natürliche Farbe: Schwarz, Schwarz/Pfauen, Schwarz/Silber<BR />
Gängige Abmessung: ab 8-18mm <BR />
Natürliche Formen: Rund, Tropfen<BR />

Auswahl der richtigen <a href="http://www.perle-schmuck.com/perlenarten-16-c0-tahitiperlen.html" title="Tahitiperlen">Tahitiperlen</a> für Schmuck<BR />
Ohrringe: 8-14mm der Normgröße, Tropf- oder runde Form, alle Farben.<BR />
Anhänger: 9.5-16mm der Normgröße, Tropf- oder runde Form, alle Farben.<BR />
Ringe: 9-14mm der Normgröße, runde Form, alle Farben.<BR />
Kette/Armbänder: 10-18mm der Normgröße, Tropf- oder runde Form, alle Farben.</p>
